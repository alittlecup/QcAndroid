public class MarkResumeBody {

  public String job_id;
  public List<String> resume_ids;
  public Integer status;

  private MarkResumeBody(Builder builder) {
    job_id = builder.job_id;
    resume_ids = builder.resume_ids;
    status = builder.status;
  }

  public static final class Builder {
    private String job_id;
    private List<String> resume_ids;
    private Integer status;

    public Builder() {
    }

    public Builder job_id(String val) {
      job_id = val;
      return this;
    }

    public Builder resume_ids(List<String> val) {
      resume_ids = val;
      return this;
    }

    public Builder status(Integer val) {
      status = val;
      return this;
    }

    public MarkResumeBody build() {
      return new MarkResumeBody(this);
    }
  }
}
