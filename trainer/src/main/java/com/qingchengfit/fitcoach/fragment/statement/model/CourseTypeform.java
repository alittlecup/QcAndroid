public class CourseTypeform implements Parcelable {

    public static final Parcelable.Creator<CourseTypeform> CREATOR = new Parcelable.Creator<CourseTypeform>() {
        @Override public CourseTypeform createFromParcel(Parcel source) {
            return new CourseTypeform(source);
        }

        @Override public CourseTypeform[] newArray(int size) {
            return new CourseTypeform[size];
        }
    };
    public int course_type;
    public int course_count;
    public int server_count;

    public CourseTypeform(int course_type, int course_count, int server_count) {
        this.course_type = course_type;
        this.course_count = course_count;
        this.server_count = server_count;
    }

    protected CourseTypeform(Parcel in) {
        this.course_type = in.readInt();
        this.course_count = in.readInt();
        this.server_count = in.readInt();
    }

    @Override public int describeContents() {
        return 0;
    }

    @Override public void writeToParcel(Parcel dest, int flags) {
        dest.writeInt(this.course_type);
        dest.writeInt(this.course_count);
        dest.writeInt(this.server_count);
    }
}
