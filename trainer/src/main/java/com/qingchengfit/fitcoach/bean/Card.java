public class Card implements Parcelable {
    public String card_name;
    public String card_tpl_id;
    public String id;
    public int card_type;

    public Card() {
    }

    protected Card(Parcel in) {
        this.card_name = in.readString();
        this.card_tpl_id = in.readString();
        this.id = in.readString();
        this.card_type = in.readInt();
    }

    @Override public int describeContents() {
        return 0;
    }

    @Override public void writeToParcel(Parcel dest, int flags) {
        dest.writeString(this.card_name);
        dest.writeString(this.card_tpl_id);
        dest.writeString(this.id);
        dest.writeInt(this.card_type);
    }
}
