public class EventChooseCourse {

    public String courseId;
    public String courseName;

    private EventChooseCourse(Builder builder) {
        courseId = builder.courseId;
        courseName = builder.courseName;
    }

    public static final class Builder {
        private String courseId;
        private String courseName;

        public Builder() {
        }

        public Builder courseId(String val) {
            courseId = val;
            return this;
        }

        public Builder courseName(String val) {
            courseName = val;
            return this;
        }

        public EventChooseCourse build() {
            return new EventChooseCourse(this);
        }
    }
}
