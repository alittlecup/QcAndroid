public class BundleBuilder {
  Bundle bundle;

  public BundleBuilder() {
    bundle = new Bundle();
  }

  public BundleBuilder add(String key,String value){
    bundle.putString(key,value);
    return this;
  }
  public BundleBuilder add(String key,Parcelable value){
    bundle.putParcelable(key,value);
    return this;
  }

  public BundleBuilder add(String key,int value){
    bundle.putInt(key,value);
    return this;
  }

  public BundleBuilder adds(String key,ArrayList<String> value){
    bundle.putStringArrayList(key,value);
    return this;
  }
  public BundleBuilder add(String key,ArrayList<Parcelable> value){
    bundle.putParcelableArrayList(key,value);

    return this;
  }


  public Bundle build(){
    return bundle;
  }

}
