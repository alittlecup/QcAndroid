public class TypeConvertUtils {
  public static int getInt(String s, int def) {
    int ret = 0;
    try {
      ret = Integer.parseInt(s);
    } catch (Exception e) {
      LogUtil.e(e.getMessage());
    }
    return ret == 0 ? def : ret;
  }
}
