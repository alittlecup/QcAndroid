public class CompetitionOrder implements Parcelable {
    public static final Parcelable.Creator<CompetitionOrder> CREATOR = new Parcelable.Creator<CompetitionOrder>() {
        @Override public CompetitionOrder createFromParcel(Parcel source) {
            return new CompetitionOrder(source);
        }

        @Override public CompetitionOrder[] newArray(int size) {
            return new CompetitionOrder[size];
        }
    };
    String price;
    String id;

    public CompetitionOrder() {
    }

    protected CompetitionOrder(Parcel in) {
        this.price = in.readString();
        this.id = in.readString();
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    @Override public int describeContents() {
        return 0;
    }

    @Override public void writeToParcel(Parcel dest, int flags) {
        dest.writeString(this.price);
        dest.writeString(this.id);
    }
}
