public class TrainIds {
    String gymId;
    String competitionId;

    private TrainIds(Builder builder) {
        gymId = builder.gymId;
        competitionId = builder.competitionId;
    }

    public String getGymId() {
        return gymId;
    }

    public void setGymId(String gymId) {
        this.gymId = gymId;
    }

    public String getCompetitionId() {
        return competitionId;
    }

    public void setCompetitionId(String competitionId) {
        this.competitionId = competitionId;
    }

    public static final class Builder {
        private String gymId;
        private String competitionId;

        public Builder() {
        }

        public Builder gymId(String val) {
            gymId = val;
            return this;
        }

        public Builder competitionId(String val) {
            competitionId = val;
            return this;
        }

        public TrainIds build() {
            return new TrainIds(this);
        }
    }
}
