@FragmentWithArgs public class ChooseTrainerFragment extends CoachListFragment {
    @Arg(required = false) String id;

    @Nullable @Override
    public View onCreateView(LayoutInflater inflater, @Nullable ViewGroup container, @Nullable Bundle savedInstanceState) {
        View view = super.onCreateView(inflater, container, savedInstanceState);
        mCallbackActivity.setToolbar("选择教练", false, null, 0, null);
        return view;
    }

    @Override public void onCreate(@Nullable Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        FragmentArgs.inject(this);
    }

    @Override public void onList(List<Staff> coach) {
        adapter.setTrainerId(id);
        super.onList(coach);
    }

    @Override public void goDetail(Staff coach) {
        Intent rst = new Intent();
        rst.putExtra("trainer", coach);
        getActivity().setResult(Activity.RESULT_OK, rst);
        getActivity().finish();
    }
}
