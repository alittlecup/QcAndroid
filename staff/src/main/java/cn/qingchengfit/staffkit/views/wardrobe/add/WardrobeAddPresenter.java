public class WardrobeAddPresenter extends BasePresenter {

    @Inject LoginStatus loginStatus;
    @Inject GymWrapper gymWrapper;
    private StaffRespository restRepository;
    private MVPView view;

    @Inject public WardrobeAddPresenter(StaffRespository restRepository) {
        this.restRepository = restRepository;
    }

    public void addWardrobe(String staffid, AddLockerBody addLockerBody) {
        if (StringUtils.isEmpty(addLockerBody.name)) {
            view.onShowError("请填写更衣柜名称");
            return;
        }

        RxRegiste(restRepository.getStaffAllApi()
            .qcAddLocker(staffid, gymWrapper.getParams(), addLockerBody)
            .observeOn(AndroidSchedulers.mainThread())
            .onBackpressureBuffer()
            .subscribeOn(Schedulers.io())
            .subscribe(new Action1<QcResponse>() {
                @Override public void call(QcResponse qcResponse) {
                    if (ResponseConstant.checkSuccess(qcResponse)) {
                        view.onAddSuccess();
                    } else {
                        view.onShowError(qcResponse.getMsg());
                    }
                }
            }, new Action1<Throwable>() {
                @Override public void call(Throwable throwable) {
                    view.onShowError(throwable.getMessage());
                }
            }));
    }

    public void getDistric() {

    }

    @Override public void attachView(PView v) {
        super.attachView(v);
        view = (MVPView) v;
    }

    public interface MVPView extends CView {
        void onAddSuccess();
    }
}
