import android.os.Bundle;

/***
 *
 * 永远不出发下拉刷新
 */
public class WebFragmentNoFresh extends WebFragment {
    public static WebFragmentNoFresh newInstance(String url) {

        Bundle args = new Bundle();
        args.putString("url", url);
        WebFragmentNoFresh fragment = new WebFragmentNoFresh();
        fragment.setArguments(args);
        return fragment;
    }

    @Override public boolean canSwipeRefreshChildScrollUp() {
        return true;
    }
}
