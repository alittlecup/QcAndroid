public class SendSmsBody {
  public String area_code;
  public String phone;

  private SendSmsBody(Builder builder) {
    area_code = builder.area_code;
    phone = builder.phone;
  }

  public SendSmsBody(String area_code, String phone) {
    this.area_code = area_code;
    this.phone = phone;
  }

  public static final class Builder {
    private String area_code;
    private String phone;

    public Builder() {
    }

    public Builder area_code(String val) {
      area_code = val;
      return this;
    }

    public Builder phone(String val) {
      phone = val;
      return this;
    }

    public SendSmsBody build() {
      return new SendSmsBody(this);
    }
  }
}
