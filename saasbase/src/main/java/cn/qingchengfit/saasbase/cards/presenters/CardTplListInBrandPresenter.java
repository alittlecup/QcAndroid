public class CardTplListInBrandPresenter extends CardTypeListPresenter {

  @Inject
  public CardTplListInBrandPresenter() {
  }

  /**
   *  按支持场馆类型 获取 0 是不限制 1 是多场馆卡  2是单场馆卡
   */
  public List<CardTpl> getCardTplByType(int type) {
    List<CardTpl> ret = new ArrayList<>();
    for (CardTpl cardTpl : cardTpls) {
      if (type == 0 || (cardTpl.shops != null && cardTpl.shops.size() > 1 && type == 1) ||(cardTpl.shops != null && cardTpl.shops.size() == 1 && type == 2) ) {
        ret.add(cardTpl);
      }
    }
    return ret;
  }

}
