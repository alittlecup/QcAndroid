public class EventLimitBuyCount {
    public int buy_count;
    public String text;

    private EventLimitBuyCount(Builder builder) {
        buy_count = builder.buy_count;
        text = builder.text;
    }

    public static final class Builder {
        private int buy_count;
        private String text;

        public Builder() {
        }

        public Builder buy_count(int val) {
            buy_count = val;
            return this;
        }

        public Builder text(String val) {
            text = val;
            return this;
        }

        public EventLimitBuyCount build() {
            return new EventLimitBuyCount(this);
        }
    }
}
