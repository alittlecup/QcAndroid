public class FunctionBean {
    @DrawableRes  public int resImg;
    public String text;
    public String subname;

    private FunctionBean(Builder builder) {
        resImg = builder.resImg;
        text = builder.text;
        subname = builder.subname;
    }


    public static final class Builder {
        private int resImg;
        private String text;
        private String subname;

        public Builder() {
        }

        public Builder resImg(int val) {
            resImg = val;
            return this;
        }

        public Builder text(String val) {
            text = val;
            return this;
        }

        public Builder subname(String val) {
            subname = val;
            return this;
        }

        public FunctionBean build() {
            return new FunctionBean(this);
        }
    }
}
