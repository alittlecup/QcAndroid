public class EventStep {
    public int step;

    private EventStep(Builder builder) {
        step = builder.step;
    }

    public static final class Builder {
        private int step;

        public Builder() {
        }

        public Builder step(int val) {
            step = val;
            return this;
        }

        public EventStep build() {
            return new EventStep(this);
        }
    }
}
