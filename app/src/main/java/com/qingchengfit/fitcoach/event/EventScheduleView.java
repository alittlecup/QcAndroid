public class EventScheduleView {
    public boolean isWeekView;
    public Date mDate;

    private EventScheduleView(Builder builder) {
        isWeekView = builder.isWeekView;
        mDate = builder.mDate;
    }

    public static final class Builder {
        private boolean isWeekView;
        private Date mDate;

        public Builder() {
        }

        public Builder isWeekView(boolean val) {
            isWeekView = val;
            return this;
        }

        public Builder mDate(Date val) {
            mDate = val;
            return this;
        }

        public EventScheduleView build() {
            return new EventScheduleView(this);
        }
    }
}
