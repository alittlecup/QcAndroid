package cn.qingchengfit.student.bean;

public class PtagAnswerOption {
  String remarks;
  String id;
  String value;
  Integer max_value;
  Integer min_value;
  Integer weight;
  String option_type;

  public String getRemarks() {
    return remarks;
  }

  public String getId() {
    return id;
  }

  public String getValue() {
    return value;
  }

  public Integer getMax_value() {
    return max_value;
  }

  public Integer getMin_value() {
    return min_value;
  }

  public Integer getWeight() {
    return weight;
  }

  public String getOption_type() {
    return option_type;
  }
}
